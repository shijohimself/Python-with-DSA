'''
\n -> new line
\b -> backspace
\" -> "
\' -> '
\\ -> \
\t -> tab (4 spaces)
'''
print("My name is\t\t\t\tShijo\nAge is 23\nGender is Male")
print("My name is \"Shijo\" Age is \"23\" Gender is Male")
print('My name is \'Shijo\' Age is \'23\' Gender is Male')

# My name is S\hijo!
print("My name is S\\hijo")

